    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String usernameSelected = jComboBox1.getSelectedItem().toString();
        Usuarios user = UsuariosManager.getInstance().findUsuarioByUsername(usernameSelected);
        if (user == null) {
            JOptionPane.showMessageDialog(null, "Usuario no encontrado", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            UsuariosManager.getInstance().removeUsuario(user);
            JOptionPane.showMessageDialog(null, "Usuario Eliminado", "Info", JOptionPane.INFORMATION_MESSAGE);
            UsuariosSubmenu menu = new UsuariosSubmenu();
        menu.setVisible(true);
        this.setVisible(false); 
        }
    }//GEN-LAST:event_jButton2ActionPerformed


    private void initUsers() {
        // Create user objects and add them to the list
        // Create user objects
        Usuarios user1 = new Usuarios("John Doe Admin", "JohnAdmin", "password123", 30, TipoUsuario.ADMINISTRADOR);
        Usuarios user4 = new Usuarios("John Doe Contenidos", "JohnContenidos", "password123", 30,
                TipoUsuario.CONTENIDOS);
        Usuarios user5 = new Usuarios("John Doe Limitado", "JohnLimitado", "password123", 30, TipoUsuario.LIMITADO);
        Usuarios user3 = new Usuarios("System", "sys", "", 25, TipoUsuario.ADMINISTRADOR);

        // Add users to the list
        ArrayList<Usuarios> usersList = new ArrayList<>();
        UsuariosManager manager = UsuariosManager.getInstance();

        if (!manager.usuarioExiste(user1.getUsername())) {
            usersList.add(user1);
            manager.addUsuario(user1);
            JOptionPane.showMessageDialog(null, "Usuario Generado Automaticamente username: " + user1.getUsername(),
                    "Info", JOptionPane.INFORMATION_MESSAGE);
        }

        if (!manager.usuarioExiste(user3.getUsername())) {
            usersList.add(user3);
            manager.addUsuario(user3);
            JOptionPane.showMessageDialog(null, "Usuario Generado Automaticamente username: "+user3.getUsername(), "Info", JOptionPane.INFORMATION_MESSAGE);
        }

        if (!manager.usuarioExiste(user4.getUsername())) {
            usersList.add(user4);
            manager.addUsuario(user4);
            JOptionPane.showMessageDialog(null, "Usuario Generado Automaticamente username: "+user4.getUsername(), "Info", JOptionPane.INFORMATION_MESSAGE);
        }

        if (!manager.usuarioExiste(user5.getUsername())) {
            usersList.add(user5);
            manager.addUsuario(user5);
            JOptionPane.showMessageDialog(null, "Usuario Generado Automaticamente username: "+user5.getUsername(), "Info", JOptionPane.INFORMATION_MESSAGE);
        }
    }


    public void login() {
        String username = jTextField1.getText();
        String password = jPasswordField1.getText();

        // Assume you have a method to validate the login credentials
        System.out.println(username + password);
        Usuarios usuario = validateLogin(username, password);

        if (usuario != null) {
            System.out.println("Login successful");
            JOptionPane.showMessageDialog(null, "Bienvenido " + usuario.getNombreCompleto());

            // Set the current user
            Usuarios.setCurrentUsuario(usuario);

            // Open the main menu
            menu menuPrincipal = new menu();
            menuPrincipal.setVisible(true);
            this.setVisible(false);
        } else {
            System.out.println("Login failed");
            JOptionPane.showMessageDialog(null, "Credenciales incorrectas. Int√©ntalo de nuevo.");
        }
    }

